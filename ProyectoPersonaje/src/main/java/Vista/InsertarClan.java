    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // agregar elementos
        String nombre=jTextField1.getText();
        int participantes=Integer.parseInt(participantesAc.getText());
        Clan clan=new Clan(nombre,participantes);
        try {
            DaoClan.insertar(clan);
        } catch (SQLException ex) {
            Logger.getLogger(InsertarClan.class.getName()).log(Level.SEVERE, null, ex);
        }
        finally{
            jTextField1.setText(" ");
            participantesAc.setText(" ");
            try {
            this.crearModelo();
        } catch (SQLException ex) {
            Logger.getLogger(ListaClan.class.getName()).log(Level.SEVERE, null, ex);
        }
        jList1.setModel(modelo);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

