    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nom=jTextField1.getText();
        String con=jTextField2.getText();
        Usuario usuario=new Usuario(nom,con);
        try {
            DaoUsuario.insertar(usuario);
        } catch (SQLException ex) {
            Logger.getLogger(Index.class.getName()).log(Level.SEVERE, null, ex);
        }
        finally{
           jTextField1.setText(" ");
           jTextField2.setText(" ");
           try {
            this.crearModelo();
        } catch (SQLException ex) {
            Logger.getLogger(Index.class.getName()).log(Level.SEVERE, null, ex);
        }
        jList1.setModel(modelo);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
       if(evt.getClickCount() == 2){
           int index = jList1.getSelectedIndex();
           Usuario usuario=(Usuario)modelo.getElementAt(index);
           try {
               DaoUsuario.eliminar(usuario);
           } catch (SQLException ex) {
               Logger.getLogger(Index.class.getName()).log(Level.SEVERE, null, ex);
           }
           finally{
                jTextField1.setText(" ");
                jTextField2.setText(" ");
                try {
                        this.crearModelo();
                } catch (SQLException ex) {
                Logger.getLogger(Index.class.getName()).log(Level.SEVERE, null, ex);
                }
                jList1.setModel(modelo);
            }
        }
       else{
           int index = jList1.getSelectedIndex();
           Usuario usuario=(Usuario)modelo.getElementAt(index); 
           jTextField1.setText(usuario.getNombre());
           jTextField2.setText(usuario.getContraseña());
           jLabel4.setText(""+usuario.getId());
       }
    }//GEN-LAST:event_jList1MouseClicked

