    private void crearModelo() throws SQLException{
        modelo.clear();
        List<Guerrero> listGuerreros=new ArrayList<>();
        listGuerreros=DaoGuerrero.asistenciaGuerrero();
        for(Guerrero personaje:listGuerreros){
            modelo.addElement(personaje);
         }
    }
