    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // actualizar
        String clan=clanAc.getText();
        int ataque=Integer.valueOf(ataqueAc.getText());
        int salud=Integer.valueOf(saludAc.getText());
        int id=Integer.valueOf(idAc.getText());
        Magico magico=new Magico(id, clan, ataque, salud);
        try {
            DaoMagico.actualizar(magico);
        } catch (SQLException ex) {
            Logger.getLogger(ActualizarMagico.class.getName()).log(Level.SEVERE, null, ex);
        }
        finally{
            idAc.setText(" ");
            clanAc.setText(" ");
            ataqueAc.setText(" ");
            saludAc.setText(" ");
            try {
            this.crearModelo();
        } catch (SQLException ex) {
            Logger.getLogger(ListaMagico.class.getName()).log(Level.SEVERE, null, ex);
        }
        jList1.setModel(modelo);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

