    private void crearModelo() throws SQLException{
        modelo.clear();
        List<Clan> listClanes=new ArrayList<>();
        listClanes=DaoClan.asistenciaClan();
        for(Clan personajeCuatro:listClanes){
            modelo.addElement(personajeCuatro);
         }
    }
